distancia = float(input('Digite uma medida em metros: '))
print('A distancia {:.2f}m em decímetros é \033[31m{:.2f}dm\033[m'.format(distancia, (distancia * 10)))
print('A distancia {:.2f}m em centímetros é \033[31m{:.2f}cm\033[m'.format(distancia, (distancia * 100)))
print('A distancia {:.2f}m em milímetros é \033[31m{:.2f}mm\033[m'.format(distancia, (distancia * 1000)))
print('A distancia {:.2f}m em micrômetros é \033[31m{:.2f}[mµm\033[m'.format(distancia, (distancia * 1000000)))
print('A distancia {:.2f}m em nanômetros é \033[31m{:.2f}[mnm\033[m'.format(distancia, (distancia * 10000000000)))
print('A distancia {:.2f}m em decâmetros é \033[31m{:.2f}[mdam\033[m'.format(distancia, (distancia / 10)))
print('A distancia {:.2f}m em hectômetros é \033[31m{:.3f}[mhm\033[m'.format(distancia, (distancia / 100)))
print('A distancia {:.2f}m em kilômetros é \033[31m{:.4f}km\033[m'.format(distancia, (distancia / 1000)))
print('A distancia {:.2f}m em megametro é \033[31m{:.6f}Mm\033[m'.format(distancia, (distancia / 1000000)))
print('A distancia {:.2f}m em gigâmetro é \033[31m{:.9f}Gm\033[m'.format(distancia, (distancia / 10000000000)))
